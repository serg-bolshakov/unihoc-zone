                            ON prices.product_id = products.id
                        LEFT JOIN sizes
                            ON products.size_id = sizes.id
                        INNER JOIN images
                            ON products.id = images.product_id
                              WHERE img_showcase = true
                        ORDER BY RAND()
                        LIMIT $notesOnPage OFFSET $fromNewPageStart
                  ";
